/**
 * 공통 프롬프트 템플릿
 * 모든 섹션에서 공통으로 사용할 프롬프트 구조
 */

export const COMMON_ENHANCEMENT_GUIDELINES = `
[공통 원칙]

1. **고유 컨텐츠 생성**
   - 공공데이터를 기반으로 하되, 단순 복사가 아닌 재구성 및 가공
   - 구글이 고유 컨텐츠로 인정할 수 있도록 구조화된 형식 사용
   - 전문성과 신뢰성을 보여주는 문체 유지

2. **가독성 향상**
   - 문단을 나누어 가독성 향상
   - 볼드(**텍스트**)를 활용하여 중요한 정보 강조
   - 목록(- 또는 •)을 활용하여 여러 항목 명확히 나열
   - 문장은 자연스럽고 읽기 쉽게 작성

3. **글자수 동적 조정**
   - 현재 글자수가 적으면 더 상세하게 작성
   - 현재 글자수가 많으면 가독성 있게 정리
   - 목표 글자수 범위 내에서 자연스럽게 완성

4. **공공데이터 기반 작성**
   - 공공데이터에 없는 정보는 추가하지 않음
   - 추측이나 일반적인 정보를 추가하지 않음
   - 제공된 정보만을 기반으로 작성

5. **전문적이면서도 친절한 톤**
   - 행정 용어를 최소화하고 시민이 이해하기 쉬운 표현 사용
   - 전문성을 유지하되 접근성 높은 문체
`;

/**
 * 글자수 안내 템플릿
 */
export function buildLengthGuidance(currentLength: number, targetMin: number, targetMax: number): string {
  return `
[글자수 안내]
- 현재 글자수: 약 ${currentLength}자
- 목표 글자수: ${targetMin}~${targetMax}자 사이
- 현재 글자수가 적으므로, 더 상세하고 구체적으로 작성하여 목표 글자수에 도달하세요.
- 문장이 자연스럽게 끝나도록 작성하세요.
`;
}

