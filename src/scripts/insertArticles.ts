async function main() {
  const hasTurso = process.env.TURSO_DATABASE_URL && process.env.TURSO_AUTH_TOKEN;
  const hasSupabase = process.env.SUPABASE_URL && process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!hasTurso && !hasSupabase) {
    throw new Error("TURSO_DATABASE_URL/TURSO_AUTH_TOKEN 또는 SUPABASE_URL/SUPABASE_SERVICE_ROLE_KEY 환경 변수가 필요합니다.");
  }

  const { getServiceClient } = await import("@lib/supabaseClient");
  const supabase = getServiceClient();

  const now = new Date().toISOString();
  const rows = articles.map((a) => ({
    slug: mkSlug(a.title),
    title: a.title,
    content: a.content,
    excerpt: a.content.replace(/\n+/g, " ").slice(0, 140).trim() + "…",
    tags: a.tags,
    is_published: true,
    published_at: now,
  }));

  const { data, error } = await supabase.from("posts").insert(rows).select("id, title");
  if (error) {
    console.error("삽입 실패:", error);
    process.exit(1);
  }

  console.log(`✅ ${data?.length}개 글 삽입 완료:`);
  data?.forEach((p) => console.log(`  - [${p.id}] ${p.title}`));
}

