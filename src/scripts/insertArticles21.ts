async function main() {
  const url = process.env.TURSO_DATABASE_URL;
  const authToken = process.env.TURSO_AUTH_TOKEN;
  if (!url) throw new Error("TURSO_DATABASE_URL is not set");

  const db = createClient({ url, authToken });

  for (const article of articles) {
    const slug = mkSlug(article.title);
    const id = crypto.randomUUID();
    const now = new Date().toISOString();
    const tags = JSON.stringify(article.tags);

    const baseTimeStr = process.env.SCHEDULE_BASE_TIME;
    const baseMs = baseTimeStr ? new Date(baseTimeStr).getTime() : Date.now();
    const INTERVAL_MS = 5 * 60 * 60 * 1000;
    const idx = articles.indexOf(article);
    const publishedAt = new Date(baseMs + idx * INTERVAL_MS).toISOString();

    await db.execute({
      sql: `INSERT INTO posts (id, slug, title, content, tags, is_published, published_at, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, 1, ?, ?, ?)
            ON CONFLICT(slug) DO NOTHING`,
      args: [id, slug, article.title, article.content, tags, publishedAt, now, now],
    });
    console.log(`✓ ${article.title}`);
  }

  console.log(`\n✅ Batch 21 완료: ${articles.length}개 삽입`);
  db.close();
}

main().catch(console.error);
