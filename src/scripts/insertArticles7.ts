const INTERVAL_MS = 5 * 60 * 60 * 1000;

async function main() {
  const db = createClient({
    url: process.env.TURSO_DATABASE_URL ?? process.env.LIBSQL_URL ?? "",
    authToken: process.env.TURSO_AUTH_TOKEN ?? process.env.LIBSQL_AUTH_TOKEN ?? "",
  });

  const baseMs = process.env.SCHEDULE_BASE_TIME
    ? new Date(process.env.SCHEDULE_BASE_TIME).getTime()
    : Date.now();

  console.log(`Inserting ${articles.length} articles from ${new Date(baseMs).toISOString()}...`);

  for (let i = 0; i < articles.length; i++) {
    const a = articles[i];
    const slug = mkSlug(a.title);
    const excerpt = a.content.replace(/[#*`>|~\[\]]/g, "").replace(/\s+/g, " ").trim().slice(0, 160);
    const tags = JSON.stringify(a.tags);
    const publishedAt = new Date(baseMs + i * INTERVAL_MS).toISOString();

    await db.execute({
      sql: `INSERT INTO posts (slug, title, content, excerpt, tags, is_published, published_at) VALUES (?, ?, ?, ?, ?, 1, ?)`,
      args: [slug, a.title, a.content, excerpt, tags, publishedAt],
    });

    console.log(`[${i + 1}/${articles.length}] ${a.title} → ${publishedAt}`);
  }

  console.log("Done.");
  db.close();
}

